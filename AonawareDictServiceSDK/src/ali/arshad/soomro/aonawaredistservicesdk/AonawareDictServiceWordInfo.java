package ali.arshad.soomro.aonawaredistservicesdk;

public class AonawareDictServiceWordInfo {

	protected String word = null;
	protected String dictId = null;
	protected String dictName = null;
	protected String wordDefination = null;
	/**
	 * @return the word
	 */
	public String getWord() {
		return word;
	}
	/**
	 * @param word the word to set
	 */
	public void setWord(String word) {
		this.word = word;
	}
	/**
	 * @return the dictId
	 */
	public String getDictId() {
		return dictId;
	}
	/**
	 * @param dictId the dictId to set
	 */
	public void setDictId(String dictId) {
		this.dictId = dictId;
	}
	/**
	 * @return the dictName
	 */
	public String getDictName() {
		return dictName;
	}
	/**
	 * @param dictName the dictName to set
	 */
	public void setDictName(String dictName) {
		this.dictName = dictName;
	}
	/**
	 * @return the wordDefination
	 */
	public String getWordDefination() {
		return wordDefination;
	}
	/**
	 * @param wordDefination the wordDefination to set
	 */
	public void setWordDefination(String wordDefination) {
		this.wordDefination = wordDefination;
	}
	
		
}
